char		*ft_flags(char * str, int *left, char *c_just)
{
	*left = 0;
	*c_just = ' ';
	if (*str == '-')
	{
		*left = 1;
		str++;
	}
	if (*str == '0')
	{
		if (*left == 0)
			*c_just = '0';
		str++;
	}
	return (str);
}
