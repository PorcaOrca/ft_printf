char		*ft_flags(char *str, int *left, char *whidt_c)
{
	if (*str == '-')
	{
		*left = 1;
		str++;
	}
	if (*str == '0')
	{
		if (!*left)
			*whidt_c = '0';
		str++;
	}
	return (str);
}
