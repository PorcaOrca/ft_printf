char		*ft_flags(char *str, int *left, char *whidt_c)
{
	while (*str == '-' || *str == '0')
	{
		if (*str == '-')
		{
			*left = 1;
			*whidt_c = ' ';
		}
		else if (!*left)
			*whidt_c = '0';
		str++;
	}
	return (str);
}
